// FUP com função de seta, que soma dois números.

const soma = (a, b) => a + b

// Testes
console.log(soma(2, 3)); // Esperado: 5
console.log(soma(-1, 4)); // Esperado: 3
console.log(soma(0, 0)); // Esperado: 0
console.log(soma(5.5, 4.5)); // Esperado: 10
console.log(soma(-5, -7)); // Esperado: -12